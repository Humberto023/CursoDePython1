# Crie uma lista notas = [7.5, 8.0, 6.5, 9.0, 5.5] e descubra:
#
# Qual a maior nota? (use max())
# Qual a menor nota? (use min())
# Quantas notas existem? (use len())
# Qual a soma de todas? (use sum())


notas = [7.5, 8.0, 6.5, 9.0, 5.5]

maior = max(notas)
menor = min(notas)

quantidade = len(notas)
somas = sum(notas)

print(maior, menor, quantidade, somas)