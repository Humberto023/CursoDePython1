# Questão 5 - Extraindo variáveis (unpacking)
# Dada a lista dados = ["João", 25, "São Paulo"], extraia as informações em variáveis separadas:
# python nome, idade, cidade = dados
# Depois imprima: "João tem 25 anos e mora em São Paulo"

